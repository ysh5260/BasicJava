package chapter02_1;

public class IntegerExample {
	public static void main(String[] args) {
		int var1 = 100;
		int var2 = 200;
		int var3 = var1 + var2;
		
		byte var4 = -128;
		int var5 = -130;
		
		long i = 100_000_000_000L; //리터럴(literal) int로 표현할 수 없기 때문에 long타입으로 표현해줌
		
		float var6 = 10.1f;
		double var7 = 10.1d;
		System.out.println(var4);
	}
}
