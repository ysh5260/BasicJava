package chapter02;

public class VariableExample {
	public static void main(String[] args) {
		// 데이터 종류(data type)를 선언할 때 알려줌
		int number1;
		int number2;
		//변수의 초기화
		number1 = 715;//대입 연산자(=): 오른쪽의 값을 왼쪽에 대입(할당)
		number2 = 328;
		
		//연산
	System.out.println(number1*number2);
		
	}
}
